      <h1>Alumnos del IES Fernando Sagaseta trabajan con madera de eucalipto rojo</h1>

      <p>Los estudiantes del ciclo de Carpintería y Mueble del IES Fernando Sagaseta han iniciado un proyecto innovador centrado en la utilización de madera de eucalipto rojo. Esta iniciativa, en colaboración con el Cabildo de Gran Canaria, tiene como objetivo explorar las propiedades de esta especie y sus aplicaciones dentro del diseño y la fabricación de mobiliario.</p>

      <p>El eucalipto rojo, conocido por su resistencia y tonalidades cálidas, se presenta como una alternativa sostenible frente a otras maderas tropicales. A través del proyecto, el alumnado no solo trabaja con un material poco habitual en la formación reglada, sino que también se familiariza con los criterios de selección y tratamiento de maderas locales.</p>

      <p>Durante las primeras fases, los estudiantes han realizado estudios sobre la densidad, la estabilidad dimensional y el comportamiento del eucalipto frente a distintas técnicas de ensamblaje. Este enfoque práctico permite vincular el aprendizaje técnico con la experimentación directa, reforzando la autonomía y la capacidad crítica del alumnado.</p>

      <p>Además, el proyecto promueve la reflexión sobre el origen de los materiales y su impacto ambiental. En un contexto donde la sostenibilidad cobra cada vez mayor relevancia, trabajar con recursos cercanos y renovables se convierte en una apuesta coherente con los objetivos de desarrollo sostenible.</p>

      <p>La implicación del Cabildo de Gran Canaria ha sido clave para facilitar el acceso a la materia prima y para establecer puentes entre el ámbito educativo y el contexto territorial. La colaboración institucional también pone en valor el papel de la formación profesional como agente activo en la transformación productiva y cultural de la isla.</p>

      <p>Según los docentes del centro, este tipo de proyectos permite al alumnado desarrollar una relación más consciente con los materiales y con su entorno. “No se trata solo de aprender una técnica, sino de entender el oficio en diálogo con el territorio y la sostenibilidad”, afirmaron durante la presentación del proyecto.</p>

      <p>El IES Fernando Sagaseta continúa así con su compromiso de integrar experiencias reales en el currículo, fomentando la creatividad, el pensamiento técnico y la conexión con el entorno natural y social. La madera de eucalipto rojo, en este caso, se convierte no solo en materia de trabajo, sino en vehículo de aprendizaje significativo.</p>
